package com.example.myapplication.model;

public class Hangeul {

    private int hangeulId;
    private String aksara;
    private String keterangan;
    private String caraBaca;
    private String penjelasanSatu;
    private String penjelasanDua;

    public int getHangeulId() {
        return hangeulId;
    }

    public void setHangeulId(int hangeulId) {
        this.hangeulId = hangeulId;
    }

    public String getAksara() {
        return aksara;
    }

    public void setAksara(String aksara) {
        this.aksara = aksara;
    }

    public String getKeterangan() {
        return keterangan;
    }

    public void setKeterangan(String keterangan) {
        this.keterangan = keterangan;
    }

    public String getCaraBaca() {
        return caraBaca;
    }

    public void setCaraBaca(String caraBaca) {
        this.caraBaca = caraBaca;
    }

    public String getPenjelasanSatu() {
        return penjelasanSatu;
    }

    public void setPenjelasanSatu(String penjelasanSatu) {
        this.penjelasanSatu = penjelasanSatu;
    }

    public String getPenjelasanDua() {
        return penjelasanDua;
    }

    public void setPenjelasanDua(String penjelasanDua) {
        this.penjelasanDua = penjelasanDua;
    }
}
